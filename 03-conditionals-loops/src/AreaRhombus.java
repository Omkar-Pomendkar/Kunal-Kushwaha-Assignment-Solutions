

public class AreaRhombus {
    public static void main(String[] args) {
        System.out.println("Calculating Area of Rhombus");
        System.out.println(rohmbus(10,10,2));
    }
    static int rohmbus(int d1,int d2,int num)
    {
        int areaRombus = d1*d2*num;
        return areaRombus;
    }
}
